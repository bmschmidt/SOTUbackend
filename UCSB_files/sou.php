                  <p class="docnotes"><img src="images/peters.jpg" alt="Gerhard Peters" width="200" height="265" hspace="10" vspace="3" align="left">State of the Union Messages to the Congress are mandated by Article II, Section 3 of the United States Constitution:  &quot;He shall from time to time give to the Congress information of the state of the union, and recommend to their consideration such measures as he shall judge necessary and expedient;&quot;&nbsp;</p>
                  <p class="docnotes">George Washington established the precedent that  clarifies the phrase &quot;from time to time.&quot;&nbsp;Since 1790, with occasional exceptions, State of the Union messages have been delivered once annually.<br>
                    <br>
                  A  misconception found even in some academic literature is that the State of the Union is an <em>orally </em>delivered message presented to a joint session of Congress. &nbsp;With  a few exceptions, this has been true in the modern era (ca. 1933-present, see Neustadt or Greenstein). However,  beginning with Jefferson's 1st State of the Union (1801) and lasting until Taft's final message (1912), the State of the Union was a written (and often lengthy) report sent to Congress to coincide with a new Session of Congress.. &nbsp;</p>
                  <p class="docnotes"> Federalists Washington and Adams had personally addressed the Congress, but Jefferson was concerned that the practice of appearing before the representatives of the people was too similar to the British monarch's practice of addressing  each new Parliament with a list of policy mandates, rather than &quot;recommendations.&quot; &nbsp;</p>
                  <p class="docnotes">Jefferson's practice  changed in 1913 with Woodrow Wilson. &nbsp;Wilson believed the presidency was more than a impersonal institution and active and visible presidential leadership was needed to the people and the Congress. As an expression of this philosophy, Wilson delivered  oral messages to Congress, citing the authority of the Constitution. &nbsp;</p>
                  <p class="docnotes">For health reasons,  Wilson did not address Congress in 1919 and 1920. Warren Harding's two messages (1921 and 1922) and Calvin Coolidge's first (1923) were also oral messages. &nbsp;Subsequently,  Coolidge's remaining State of the Unions (1924-28) and all four of Hoover's (1929-32) were written. &nbsp;</p>
                  <p class="docnotes">Franklin D. Roosevelt consolidated the modern practice of delivering an oral State of the Union beginning with his first in 1934. &nbsp;Exceptions include Truman's 1st (1946) and last (1953), Eisenhower's last (1961), Carter's last (1981), and Nixon's 4th (1973 and 1974 when he submitted multiple documents entitled &quot;State of the Union.&quot;). &nbsp;In addition, Roosevelt's last (1945) and Eisenhower's 4th (1956) were technically written messages although they addressed the American people via radio summarizing their reports. &nbsp;In 1972 and 1974, Nixon  presented both an oral address and a written message as did Carter in 1978, 1979, and 1980. Scholarly research needs to recognize the variability in these practices.<br>
                    <br>
                  The five most recent presidents (Reagan, Bush, Clinton, G.W. Bush, and Obama) addressed a joint session of Congress shortly after their inaugurations but these messages are technically not considered to be &quot;State of the Union&quot; addresses. Reagan's 1981 address is called, "Address Before a Joint Session of the Congress on the Program for Economic Recovery." &nbsp;Bush's 1989 and Clinton's 1993 messages are called &quot;Administration Goals&quot; speeches. &nbsp;</p>
                  <p class="docnotes">G.W. Bush's 2001 speech was actually his &quot;Budget Message,&quot; and President Obama delivered a similar non-State of the Union address to a joint session of Congress on February 24, 2009.&nbsp;For research purposes, it is probably harmless to categorize these as State of the Union messages. Tthe impact of such a speech on public, media, and congressional perceptions of presidential leadership and power should be the same as if the address was an official State of the Union. &nbsp;These speeches are included in the table below with an asterisk.<br>
                    <br>
                An additional fact is that the State of the Union is delivered near the beginning of each session of Congress. &nbsp;Before 1934 this meant the State of the Union was delivered usually in December.  Since 1934, the State of the Union has been delivered near the beginning each year, with some presidents delivering a final message at the end of their last term (Truman, Eisenhower, Johnson, Ford, and Carter). &nbsp;The table below reflects each message's placement in the President's term.&quot;<br><br>
                President George W. Bush delivered his last State of the Union Address on January 28, 2008.  Bush had the right to deliver either a written or oral State of the Union in the days immediately before leaving office in 2009. However, like Presidents Reagan, George H.W. Bush, and Clinton, he chose not to do so.  Presidents Truman, Eisenhower, Johnson, Ford, and Carter chose to do so.</p></td>
